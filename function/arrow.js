// event
function handleArrowClick(arrow, optionRow) {
  if (optionRow.style.display === 'none') {
    optionRow.style.display = 'grid'; // 
    arrow.textContent = 'arrow_drop_up'; // 更改箭头方向为向上
  } else {
    optionRow.style.display = 'none'; // 隐藏对应的 optionRow
    arrow.textContent = 'arrow_drop_down'; // 更改箭头方向为向下
  }
}

